//[ ] Faça um programa que receba 3 números inteiros, e deve imprimir, qual é o menor, qual é o menor.

const numberOne = 20
const numberTwo = 9
const numberThree = 89

if (numberOne > numberTwo && numberOne > numberThree) {
    console.log('O primeiro é o maior número')
}

else if (numberTwo > numberThree) {
    console.log('O segundo é o maior número')
}

else {
    console.log('O terceiro é o maior número')
}

if (numberOne < numberTwo && numberOne < numberThree) {
    console.log('O primeiro é o menor número')
}

else if (numberTwo < numberThree) {
    console.log('O segundo é o menor número')
}

else {
    console.log('O terceiro é o menor número')
}


